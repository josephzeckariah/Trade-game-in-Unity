using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class UiScreen : MonoBehaviour
{
	///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//-//////////////////////////////////////////////////////////////////////////////////////////////////       Memories       ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	[HideInInspector]
    public UiChangeManager ourUiChangeManager;


	///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//+///////////////////////////////////////////////////////////////////////////////////////////////         Actions        //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	public void OpeningScreenStartButtonButtonClicked()
	{
		ourUiChangeManager.NormalStartButtonClicked();	
	}


	public void EndingScreenContinueButtonClicked()
	{
		ourUiChangeManager.MoveFromEndScreenToCreditScreen();
	}



	public void CreditScreenContinueButtonClicked()
	{
		ourUiChangeManager.MoveFromCreditScreenBackToStartScreenWithExplainer();
	}
}
