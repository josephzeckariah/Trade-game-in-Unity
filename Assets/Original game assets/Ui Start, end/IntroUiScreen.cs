using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class IntroUiScreen : UiScreen
{
	///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//-//////////////////////////////////////////////////////////////////////////////////////////////////       Memories       ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	public bool willIntroduceMinGmae ;

	///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//+///////////////////////////////////////////////////////////////////////////////////////////////         Actions        //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	public void IntroEndButtonCLicked()
	{
		ourUiChangeManager.IntroEnded();

		if(willIntroduceMinGmae )
		{
			if (GameStateInformationProvider.NormalGameStart != null)
			{
				GameStateInformationProvider.NormalGameStart();                             //       ------------------------------------------------>>>
			}
		}
		else
		{
			if (GameStateInformationProvider.NewGamePlusStart != null)
			{
				GameStateInformationProvider.NewGamePlusStart();                             //       ------------------------------------------------>>>
			}
		}
	}

}
