using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class CreditScreen : UiScreen
{
	///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//-//////////////////////////////////////////////////////////////////////////////////////////////////       Memories       ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	public List<GameObject> firstScreen = new List<GameObject>();
	public List<GameObject> SecondtScreen = new List<GameObject>();


	[HideInInspector]
	public CreditScreenType creditScreenType;


	///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//+///////////////////////////////////////////////////////////////////////////////////////////////         Actions        //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////


	//I///////////////////////////////////////////////////////////     back --- next      /////////////////////////////////////////////////////////////
	public void OnNextButtonClick()
	{
		foreach (GameObject objects in firstScreen) 
		{
			objects.SetActive(false);
		}
		foreach (GameObject objects in SecondtScreen)
		{
			objects.SetActive(true);
		}
	}
	public void OnBackButtonClick()
	{
		foreach (GameObject objects in SecondtScreen)
		{
			objects.SetActive(false);
		}
		foreach (GameObject objects in firstScreen)
		{
			objects.SetActive(true);
		}
	}





	//S///////////////////////////////////////////////////////////     Continue       /////////////////////////////////////////////////////////////
	public void CreditScreenContinueButtonClicked()
	{
		switch (creditScreenType)
		{
			case CreditScreenType.EndCredit:
				ourUiChangeManager.MoveFromCreditScreenBackToStartScreenWithExplainer();
				break;
			case CreditScreenType.openingCredit:
				ourUiChangeManager.MoveFromCreditScreenBackToStartScreenWithoutAnyExplaner();
				break;
		}

	}
}
