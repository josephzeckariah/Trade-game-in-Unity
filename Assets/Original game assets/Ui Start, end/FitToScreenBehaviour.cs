using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class FitToScreenBehaviour : MonoBehaviour
{
	///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//-//////////////////////////////////////////////////////////////////////////////////////////////////       Memories       ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	[HideInInspector]
	public Canvas ourCanvasToFitIn;


	///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//+///////////////////////////////////////////////////////////////////////////////////////////////         Actions        //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////


	//I///////////////////////////////////////////////////////////     Initalize       /////////////////////////////////////////////////////////////
	private void Awake()
	{
		
		
		ourCanvasToFitIn = this.GetComponentInParent<Canvas>();

		GameStateInformationProvider.ScreenSizeChanged += RepoSitionOpeningSign;
	}



	//S///////////////////////////////////////////////////////////     Start       /////////////////////////////////////////////////////////////
	void Start()
	{
		RepoSitionOpeningSign(Vector2.zero);
	}


	//OA///////////////////////////////////////////////////////////     Ocational actions      /////////////////////////////////////////////////////////////
	public void RepoSitionOpeningSign(Vector2 newSize)
	{

		this.GetComponent<RectTransform>().sizeDelta = ourCanvasToFitIn.GetComponentInParent<RectTransform>().sizeDelta;
	}



	//OA///////////////////////////////////////////////////////////     On destory      /////////////////////////////////////////////////////////////
	private void OnDestroy()
	{
		GameStateInformationProvider.ScreenSizeChanged -= RepoSitionOpeningSign;
	}


}
