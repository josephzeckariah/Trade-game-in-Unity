using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class GeneralInformationProvider : MonoBehaviour
{
	///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//-//////////////////////////////////////////////////////////////////////////////////////////////////       Memories       ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	[Header("Manual connections")]
	public GameObject needSighnTemplateSHowInspector;
	public Sprite FoodSprite;
	public Sprite MaterialsSprite;                                                            //set by inspector General game data
	public Sprite ElectronicsSprite;
	public Sprite MachinesSprite;

	public Sprite SmileSprite;
	public Sprite MoneySprite;


	///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//-//////////////////////////////////////////////////////////////////////////////////////////////////       Information provided       ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	//public memory
	public static Sprite Smile;
	public static Sprite Money;
	public static Dictionary<Needs, Sprite> needsAssets = new Dictionary<Needs, Sprite>();
	public static GameObject needSighnTemplate;

	public static Dictionary<string, string> needsNamesAndTranslation = new Dictionary<string, string>();
	public static bool gameIsOnMobile;
	//public static Dictionary<Country, string>  countryNamesAndTranslation = new Dictionary<Country, string>();

	///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//+///////////////////////////////////////////////////////////////////////////////////////////////         Actions        //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	public void AssignFacts()
	{
		AssighnSprites();

		AssighnNeedNames();

		StartCoroutine(CheckIfGameIsOnMobile());
	}
	                      void AssighnSprites()
	{
		needSighnTemplate = needSighnTemplateSHowInspector;


		needsAssets.Add(Needs.Food, FoodSprite);
		needsAssets.Add(Needs.Materials, MaterialsSprite);
		needsAssets.Add(Needs.Electronics, ElectronicsSprite);
		needsAssets.Add(Needs.Machines, MachinesSprite);

		Money = MoneySprite;
		Smile = SmileSprite;
	}

	                      void AssighnNeedNames()
	{
		needsNamesAndTranslation.Add(Needs.Food.ToString(), "الطعام");
		needsNamesAndTranslation.Add(Needs.Materials.ToString(), "الخامات");
		needsNamesAndTranslation.Add(Needs.Electronics.ToString(), "لإلكترونيات");
		needsNamesAndTranslation.Add(Needs.Machines.ToString(), "الماكينات");
	}


	                       IEnumerator CheckIfGameIsOnMobile()
	{
		while (true)
		{
			if(Input.touchCount> 0)
			{
				gameIsOnMobile = true;
				yield break;
			}

			yield return null;
		}
	}


}
