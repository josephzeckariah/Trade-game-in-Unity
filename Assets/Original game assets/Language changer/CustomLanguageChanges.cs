using System.Collections;
using System.Collections.Generic;
using UnityEngine;
using UnityEngine.UI;

public class CustomLanguageChanges : MonoBehaviour
{
	///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//-//////////////////////////////////////////////////////////////////////////////////////////////////       Memories       ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	
	///////////////////////////////////////////////////////////// Manual properties
	[SerializeField]
	Sprite imageIfEnglish;
	[SerializeField]
	Sprite imageIfArabic;


	///////////////////////////////////////////////////////////// Auto connections
	Image ourImageToChange;

	///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//+///////////////////////////////////////////////////////////////////////////////////////////////         Actions        //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	private void Awake()
	{
		GameStateInformationProvider.LanguageChanged += ChangeImageCcordingToLanguage;
		ourImageToChange = GetComponent<Image>();
	}

	private void Start()
	{
		ChangeImageCcordingToLanguage(GameStateInformationProvider.currentLanguageSelected);
	}


	void ChangeImageCcordingToLanguage(LanguagesInGame languageToChangeInto)
	{
		switch (languageToChangeInto)
		{
			case LanguagesInGame.English:
				ourImageToChange.sprite = imageIfEnglish;
				break;
			case LanguagesInGame.Arabic:

				ourImageToChange.sprite = imageIfArabic;
				break;
		}

	}

	private void OnDestroy()
	{
		GameStateInformationProvider.LanguageChanged -= ChangeImageCcordingToLanguage;
	}
}
